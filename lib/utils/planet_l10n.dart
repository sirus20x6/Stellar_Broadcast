/// Maps the English tier string returned by [Planet.tier] to a localized label.
String localizedTier(AppLocalizations l10n, String tier) => switch (tier) {
  'Utopia' => l10n.planet_tierUtopia,
  'Paradise' => l10n.planet_tierParadise,
  'Habitable' => l10n.planet_tierHabitable,
  'Harsh' => l10n.planet_tierHarsh,
  'Hostile' => l10n.planet_tierHostile,
  'Death World' => l10n.planet_tierDeathWorld,
  _ => tier,
};

